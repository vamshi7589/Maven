package demo;

public class Loops {

	public static void main(String[] args) {
		
		
		for(int i=0;i<=10; i++)
		{
			
			System.out.println(i);
		}
		
		System.out.println("*************************************");
		
		for (char c ='A';c <='Z' ;c++)
		{
			System.out.print(c);
		}
		System.out.println("*************************************");
		
	}
}

